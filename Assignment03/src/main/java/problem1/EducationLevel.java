/**
 * Enumeration representing different education levels.
 */
public enum EducationLevel {

  /**
   * High school diploma education level.
   */
  High_School_Diploma,

  /**
   * Some college education level.
   */
  Some_College,

  /**
   * Associate's degree education level.
   */
  Associates_Degree,

  /**
   * Bachelor's degree education level.
   */
  Bachelors_Degree,

  /**
   * Master's degree education level.
   */
  Masters_Degree,

  /**
   * Doctoral education level.
   */
  Doctoral,

  /**
   * Professional degree education level.
   */
  Professional_Degree
}
