package model;

/**
 * The RideType enum represents different types of rides that can be requested.
 */
public enum RideType {
  /**
   * Represents an express ride type.
   */
  Express,

  /**
   * Represents a standard ride type.
   */
  Standard,

  /**
   * Represents a wait-and-save ride type.
   */
  WaitAndSave,

  /**
   * Represents an environmentally conscious ride type.
   */
  EnvironmentallyConscious
}
